package operators;

public class Ternary {
	
	public static void main(String[] args) {
		
		// if else is replacement of ternary operator 
		// syntax : (condition) ? (if) true statement :(else) false  statement [? :]
		var a = 10;
		var b = 5;
		
		System.out.println( a>b ? "upendra" : "Dupendra");
	}

}
