package dTypes;

public class NonPrimitiveDataTypes {

	public static void main(String[] args) {
		
		// String
        String name = "Upendra";
        System.out.println("String: " + name);

        // Array
        int[] numbers = {1, 2, 3, 4, 5};
        System.out.print("Array: ");
        for (int n : numbers) {
            System.out.print(n + " ");
        }

	}

}
